                                        <div class="blog-title text-start">
                                            <h2>What Are Welded Pipes?</h2>
                                        </div>

                                        <div class="text">
                                            <p>
                                                <a href="{{ route('products.pipes-tubes.welded-pipes') }}">Welded pipes</a>
                                                are made by rolling a flat sheet of metal into a tube and
                                                welding the edges together.
                                            </p>
                                        </div>

                                        <div class="exapmle">
                                            <h3>How they’re made:</h3>
                                            <ul>
                                                <li>
                                                    <strong>Forming:</strong> A flat sheet of stainless or carbon steel is
                                                    rolled into a tube
                                                    shape.
                                                </li>
                                                <li>
                                                    <strong>Welding:</strong> The edges are joined using techniques like
                                                    Electric Resistance
                                                    Welding (ERW) or High Frequency Induction Welding (HFI).
                                                </li>
                                                <li>
                                                    <strong>Finishing:</strong> The pipe is heat treated, cleaned, and
                                                    inspected for quality.
                                                </li>
                                            </ul>
                                        </div>
                                        <div class="exapmle">
                                            <h3>Why choose welded pipes?</h3>
                                            <ul>
                                                <li>
                                                    <strong>Cost effective:</strong> They’re more affordable than seamless
                                                    pipes, especially for larger diameters.
                                                </li>
                                                <li>
                                                    <strong>Quick production:</strong> Shorter lead times mean faster
                                                    delivery.
                                                </li>
                                                <li>
                                                    <strong>Customizable:</strong> Available in a variety of lengths and
                                                    thicknesses.
                                                </li>
                                            </ul>
                                        </div>
                                        <div class="text pt-2">
                                            <p>
                                                <strong class="bold">Common uses:</strong> Water supply systems,
                                                structural applications, fencing, and chemical transport lines.
                                            </p>
                                        </div>
                                    </div>
                                </section>
                                <section id="blog-area" class="blog-with-sidebar-area blog-single-area">
                                    <div class="text-holder">
                                        <div class="blog-title text-start">
                                            <h2>What Are Seamless Pipes?</h2>
                                        </div>

                                        <div class="text">
                                            <p>
                                                <a href="{{ route('products.pipes-tubes.seamless-pipes') }}">Seamless
                                                    pipes</a> are made from a solid piece of steel that’s heated and
                                                pierced to create a hollow tube no welding required.
                                            </p>
                                        </div>

                                        <div class="exapmle">
                                            <h3>How they’re made:</h3>
                                            <ul>
                                                <li>
                                                    <strong>Billet Preparation:</strong> High quality steel billets are
                                                    selected.
                                                </li>
                                                <li>
                                                    <strong>Piercing:</strong> A rotary piercer creates a hollow center.
                                                </li>
                                                <li>
                                                    <strong>Rolling & Stretching:</strong> The hollow billet is stretched
                                                    and rolled to the desired size.
                                                </li>
                                            </ul>
                                        </div>
                                        <div class="exapmle">
                                            <h3>Why choose seamless pipes?</h3>
                                            <ul>
                                                <li>
                                                    <strong>Stronger:</strong> No welded seam means uniform strength all
                                                    around
                                                <li>
                                                    <strong>High performance:</strong> Perfect for high pressure and
                                                    high-temperature applications.
                                                </li>
                                                <li>
                                                    <strong>Precision:</strong> Superior dimensional accuracy and a smooth
                                                    finish.
                                                </li>
                                            </ul>
                                        </div>
                                        <div class="text pt-2">
                                            <p>
                                                <strong class="bold">Common uses:</strong> Oil and gas pipelines, power
                                                generation boilers, aerospace, and chemical processing.
                                            </p>
                                        </div>
                                    </div>
                                </section>







